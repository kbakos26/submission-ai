export function generateAcord130PDF(data: any): void {
  const doc = new jsPDF();
  let y = addHeader(doc, '130', 'Workers Compensation Application');
  
  // WC Summary
  y = addSection(doc, 'Workers Compensation Summary', y);
  const summarySection = data.sections?.find((s: any) => s.name.includes('Summary'));
  if (summarySection?.fields) {
    summarySection.fields.forEach((field: any) => {
      y = addField(doc, field.label, field.value, y);
    });
  }
  y += 10;
  
  // Classification Codes Table
  if (y > 200) {
    doc.addPage();
    y = 20;
  }
  y = addSection(doc, 'Classification Codes', y);
  const classSection = data.sections?.find((s: any) => s.name.includes('Classification'));
  if (classSection?.fields && classSection.fields.length > 0) {
    const tableData = classSection.fields.map((f: any) => [
      f.code || '',
      f.description || f.label || '',
      f.payroll || '',
      f.rate || '',
      f.premium || ''
    ]);
    y = addTable(doc, ['Code', 'Description', 'Payroll', 'Rate', 'Premium'], tableData, y);
  }
  
  doc.save('ACORD-130-Workers-Comp.pdf');
}

