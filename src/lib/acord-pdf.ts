import jsPDF from 'jspdf';

function addHeader(doc: jsPDF, formNumber: string, formName: string) {
  doc.setFontSize(16);
  doc.setFont('helvetica', 'bold');
  doc.text(`ACORD ${formNumber} — ${formName}`, 20, 20);
  
  doc.setFontSize(8);
  doc.setFont('helvetica', 'normal');
  doc.text('Generated by SubmissionAI', 200, 10, { align: 'right' });
  doc.text(new Date().toLocaleDateString(), 200, 15, { align: 'right' });
  
  return 30; // Starting Y position for content
}

function addSection(doc: jsPDF, title: string, y: number): number {
  doc.setFontSize(12);
  doc.setFont('helvetica', 'bold');
  doc.text(title, 20, y);
  doc.setLineWidth(0.5);
  doc.line(20, y + 2, 190, y + 2);
  return y + 10;
}

function addField(doc: jsPDF, label: string, value: string, y: number, x: number = 20): number {
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(100, 100, 100);
  doc.text(`${label}:`, x, y);
  
  doc.setTextColor(0, 0, 0);
  doc.setFont('helvetica', 'normal');
  const lines = doc.splitTextToSize(value || 'N/A', 80);
  doc.text(lines, x + 60, y);
  
  return y + (lines.length * 5) + 3;
}

function addTable(doc: jsPDF, headers: string[], rows: any[][], y: number): number {
  doc.setFontSize(8);
  doc.setFont('helvetica', 'bold');
  
  // Headers
  let x = 20;
  const colWidth = (190 - 20) / headers.length;
  headers.forEach((header, i) => {
    doc.text(header, x + (i * colWidth), y);
  });
  
  doc.setLineWidth(0.3);
  doc.line(20, y + 2, 190, y + 2);
  
  y += 8;
  doc.setFont('helvetica', 'normal');
  
  // Rows
  rows.forEach(row => {
    row.forEach((cell, i) => {
      doc.text(String(cell || ''), x + (i * colWidth), y);
    });
    y += 6;
  });
  
  return y + 5;
}

export function generateAcord125PDF(data: any): void {
  const doc = new jsPDF();
  let y = addHeader(doc, '125', 'Commercial Insurance Application');
  
  // Agency Information
  y = addSection(doc, 'Agency Information', y);
  if (data.sections) {
    const agencySection = data.sections.find((s: any) => s.name === 'Agency Information');
    if (agencySection?.fields) {
      agencySection.fields.forEach((field: any) => {
        y = addField(doc, field.label, field.value, y);
      });
    }
  }
  y += 5;
  
  // Named Insured
  y = addSection(doc, 'Named Insured', y);
  const insuredSection = data.sections?.find((s: any) => s.name === 'Named Insured');
  if (insuredSection?.fields) {
    insuredSection.fields.forEach((field: any) => {
      y = addField(doc, field.label, field.value, y);
    });
  }
  y += 5;
  
  // Business Information
  y = addSection(doc, 'Business Information', y);
  const businessSection = data.sections?.find((s: any) => s.name === 'Business Information');
  if (businessSection?.fields) {
    businessSection.fields.forEach((field: any) => {
      y = addField(doc, field.label, field.value, y);
      if (y > 270) {
        doc.addPage();
        y = 20;
      }
    });
  }
  y += 5;
  
  // Prior Carrier
  if (y > 240) {
    doc.addPage();
    y = 20;
  }
  y = addSection(doc, 'Prior Carrier', y);
  const priorSection = data.sections?.find((s: any) => s.name === 'Prior Carrier');
  if (priorSection?.fields) {
    priorSection.fields.forEach((field: any) => {
      y = addField(doc, field.label, field.value, y);
    });
  }
  
  doc.save('ACORD-125-Application.pdf');
}

